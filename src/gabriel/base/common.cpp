#include "gabriel/base/common.hpp"

namespace gabriel {
namespace base {

Point::Point(int32 x, int32 y)
{
    m_x = x;
    m_y = y;
}

Point::Point()
{
    m_x = 0;
    m_y = 0;
}

int32 Point::hash() const
{
    return m_x * 10000 + m_y;        
}

bool Point::operator==(const Point &pos) const
{
    return m_x == pos.m_x && m_y == pos.m_y;
}    
    
}
}
